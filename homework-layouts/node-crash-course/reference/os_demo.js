const os = require('os');

// PLATFORM
console.log(os.platform());

// CPU ARCHETECTURE
console.log(os.arch());

// CPU CORE INFO
console.log(os.cpus());

// FREE MEMORY
console.log(os.freemem());

// TOTAL MEMORY
console.log(os.totalmem());

// HOME DIRECTORY
console.log(os.homedir());

// SYSTEM UPTIME
console.log(os.uptime());